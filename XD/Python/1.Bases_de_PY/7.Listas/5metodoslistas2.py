lista = [5,2,1,4,3]

print(lista.count(5)) #busca la cantidad de veces que aparece lo que le mandemos de parametro
print(lista.index(4)) #recibe un parametro y lo busa en la lista, retorna la posicion del primero que encuentre
lista.sort() #no lleva parametro y ordena la lista en orden ascendente 
print(lista)
lista.reverse() #ordena la lista en orden decentente 
print(lista)