#Se refiere a palabras reservadas del lenguaje que cambian el comportamiento de los strings
cadena = 'Estoy Utilizando los metodos de Py'
cadena2 = 'ESTOY UTILIZANDO LOS METODOS DE PY'
# metodo que transforma mayusculas en minusculas 
print(cadena2.lower()) #la sintaxis utilizada es: variable, seguida de un punto, seguida del nombre del metodo y parentesis
print(cadena.upper()) #convierte a mayusculas las minusculas
print(cadena.capitalize()) #si la primer letra esta en minuscula la convierte a una mayuscula, solo la primera
print(cadena.title()) #primer letra de cada palabra en mayuscula
print(cadena.swapcase()) #LAS MINUSCULAS A MAYUSCULAS Y VICEVERSA 



