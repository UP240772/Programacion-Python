cadena = 'Estoy mostrando los metodos booleanos para las strings'
cadena2 = 'KSJDLSsKFNLSK'
cadena3 = 'skdjfskjndaldn'
# Metodo startswith() 
print(cadena.startswith('E'))#identifica si la cadena comienza con el parametro que le mandamos a startswith
#Metodo endswith()
print(cadena.endswith('s'))#identifica si la cadena termina con el dato en el parametro de endswith
#Metodo isupper()
print(cadena2.isupper()) #identifica si toda la cadena esta en mayusculas 
#Metodo islower()
print(cadena3.islower()) #identifica si todo esta en minuscula 